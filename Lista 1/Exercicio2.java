// Fazer um programa que imprime os números de 1 a 5 na tela. Usando while (teste) { instruções do bloco }

public class Exercicio2 {

    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i = i + 1;
        }
    }
}
