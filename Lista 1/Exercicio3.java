//  Fazer um programa que imprime os números  de 1 a 5 na tela. Usando do { instruções do bloco } while( teste );

public class Exercicio3 {

    public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println(i);
            i = i + 1;
        } while (i <= 5);
    }
}
