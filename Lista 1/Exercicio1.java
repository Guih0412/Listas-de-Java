// Fazer um programa que imprime os números de 1 a 5 na tela. Usando for (início; teste; incremento ) { instruções do bloco }

public class Exercicio1 {

    public static void main(String[] args) {
        for (int i = 1; i <= 5; i = i + 1) {
            System.out.println(i);
        }
    }
}
